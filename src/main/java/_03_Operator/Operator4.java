package _03_Operator;

public class Operator4 {
    public static void main(String[] args) {
        //복합대입연산자
        int a = 5;

        // 우변먼저 연산하고, 최종대입
        a += 3; // a를 3증가 시킨것
        a -= 3; // a를 3감소 시킨것
        a *= 3; // a를 3곱하기 시킨것
        a /= 3; // a를 3나누기 시킨것
        a %= 3; // a를 3으로 나머지 연산한 결과를 대입










    }
}
